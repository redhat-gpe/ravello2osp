The convert.sh script is the ravello2osp "easy mode" script.
You will need to set up a credentials file before using it.  There is an example called creds.inc.example.
Take a look at that file and input your credentials for OSP and Ravello.  You should also point to your public SSH key to streamline the process.
NOTE: If you do not have the required credentials you will need to contact the administrators of the service to request access.

Usage is as simple as this:

cd <location of convert.sh>
./convert.sh <blueprint-name>

Enjoy.
